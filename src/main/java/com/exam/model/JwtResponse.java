package com.exam.model;

public class JwtResponse {
    String token;
    User user;

    public JwtResponse() {
    }

    public JwtResponse(String token, User user) {
        this.token = token;
        this.user = user;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
